void showValue()
{
  unsigned long start = millis();
  co2 = mhz.getCO2();
  unsigned long duration = millis() - start;

  Serial.print(F("CO2: "));
  Serial.println(co2);
  Serial.print(F("Duration: "));
  Serial.println(duration);
  Serial.println();
}

void showDht()
{
  hudm = dht.readHumidity();
  temp = dht.readTemperature();
  Serial.println(String("Temperature: ") + String(temp) + String(" Humidity: ") + String(hudm));
}

void checkButton()
{
  if(analogRead(A0) > 900) butState = 1;
  else butState = 0;
}

void setup()
{
  Serial.begin(115200);
  Serial.println();
  Serial.println("setup");
delay(7000);
  display.init(); // enable diagnostic output on Serial
  display.setRotation(1);
dht.begin();
  mhz.useLimit(5000);
  pinMode(A0, INPUT);
  Serial.println("setup done");
}

void loop()
{
  delay(2000);
  showFont("FreeMonoBold9pt7b", &FreeMonoBold9pt7b);
  showDht();
  showValue();
  checkButton();
  delay(3000);
}

