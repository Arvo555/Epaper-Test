  display.setTextColor(GxEPD_BLACK);
  display.setFont(f);
  display.setCursor(0, 15);
  display.println(String("Temp: ") + String(temp));
  display.println(String("Hudm: ") + String(hudm));
  display.println(String("CO2: ") + String(co2));
  display.println(String("Button: ") + String(butState));
  
  
